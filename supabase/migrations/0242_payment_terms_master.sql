-- ============================================================================
-- Raagam ERP — 0242 Master Data ▸ Associates ▸ Payment Terms
-- Legacy EDP2 "Payment term" form: Entry No (auto) · Date · Pay Mode · an
-- "AT" phrase of 3 dropdowns (basis · when · event) · With Interest · Credit
-- Days · Description · Blocked. Flat header master (same shape as receivable
-- terms 0237, distinct table). RLS = masters.
-- ============================================================================

create table if not exists public.payment_terms (
  id            uuid primary key default gen_random_uuid(),
  entry_no      bigint generated by default as identity,
  entry_date    date not null default current_date,
  pay_mode      text check (pay_mode is null or pay_mode in (
                  'CHEQUE','DA','DD','DP','LC','OTH','PDC','TT')),
  at_basis      text check (at_basis is null or at_basis in ('SIGHT','OPEN','DAYS')),
  at_when       text check (at_when is null or at_when in ('AFTER','FROM')),
  at_event      text check (at_event is null or at_event in (
                  'SHIPMENT DATE','BL DATE','SIGHT','RECEIPT OF DOCUMENTS',
                  'INVOICE DATE','LC OPEN DATE')),
  with_interest boolean not null default false,
  credit_days   integer not null default 0,
  description   text,
  blocked       boolean not null default false,
  created_at    timestamptz not null default now(),
  updated_at    timestamptz not null default now()
);
create trigger trg_payment_terms_updated before update on public.payment_terms
  for each row execute function public.set_updated_at();

-- ---------- RLS (read open like other masters; write gated by 'masters') ----------
do $$
begin
  execute $f$
    create policy payment_terms_read on public.payment_terms for select to authenticated using (true);
    create policy payment_terms_insert on public.payment_terms for insert to authenticated with check (public.has_permission('masters','create'));
    create policy payment_terms_update on public.payment_terms for update to authenticated using (public.has_permission('masters','edit')) with check (public.has_permission('masters','edit'));
    create policy payment_terms_delete on public.payment_terms for delete to authenticated using (public.has_permission('masters','delete'));
  $f$;
  alter table public.payment_terms enable row level security;
end $$;
